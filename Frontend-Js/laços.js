console.log('Números pares:');
let num = 1;
while (num <= 10) {
    if (num % 2 == 0) {
        console.log(num);
    }
    num++;
    //num--; de trás para frente
}

console.log('Números ímpares:');
let num2 = 1;
while (num2 <= 10) {
    if (num2 % 2 != 0) {
        console.log(num2);
    }
    num2++;
    //num2--; de trás para frente
}

